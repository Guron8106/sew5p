def fromStrings(mapsItem):
    """
    Converting String maze to List

    :param mapsItem: String of Maze
    :return: list
    """
    return [list(row) for row in mapsItem]


def printLabyrinth(lab):
    """
    Print the Labyrinth

    :param lab:  Labyrinth
    :return: print Maze
    """
    for row in lab:
        print("".join(row))
